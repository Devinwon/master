# Python练习题要求如下：
# 简述：话说有一对可爱的兔子，出生后的第三个月开始，每一月都会生一对小兔子。
# 当小兔子长到第三个月后，也会每个月再生一对小小兔子.
# 问题：假设条件，兔子都不死的情况下，问每个月的兔子总数为多少？
# Python解题思路分析：兔子的规律为数列1,1,2,3,5,8,13,21....，好似斐那波契数列的感觉哦~
a1 = 1
b2 = 1
for i in range(1,10):
    print('%12ld %12ld' %(a1,b2),end='')
    if (i%3==0):
        print("\n")
    a1 = a1 + b2
    b2 = a1 + b2
